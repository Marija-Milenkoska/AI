"""Да се напише функција која за дадена листа од торки во облик
[('a', 1), ('b', 2), ('c', 3)] ќе направи swap на елементите во
торките така што елементот на позиција 0 ќе биде елемент на позиција 1 и обратно.
Да се користи list comprehension."""


def swap_tuples(tuples):
    return [(b, a) for a, b in tuples]


if __name__ == "__main__":
    print(swap_tuples([('a', 1), ('b', 2), ('c', 3)]))
